/*
** ft_isascii: возвращает ненулевое значение,
** если значение ch находится в пределах
** от 0 до 0x7F, в противном случае возвращается 0.
*/

int	ft_isascii(int c)
{
	return (((c >= 0) && (c <= 0x7F)) ? 1 : 0);
}
